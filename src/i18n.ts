function safeLanguage(language: AppLanguage | null | undefined): AppLanguage {
  return language === "en" || language === "tr" ? language : "en";
}

export function t(language: AppLanguage | null | undefined, key: keyof typeof copy["en"]) {
  return copy[safeLanguage(language)][key] ?? copy.en[key];
}

export function tf(language: AppLanguage | null | undefined, key: keyof typeof copy["en"], values: Record<string, string | number>) {
  return Object.entries(values).reduce<string>((text, [token, value]) => text.replace(`{${token}}`, String(value)), t(language, key));
}
